        <li><div class="ques">What is Coin Silver?</div></li>
        <li><div class="ans">Coin silver refers to silverware produced in America from early colonial times to just after the Civil War. Items of coin silver produced during this time period are over ninety percent pure silver and were made from European silver coins. It is sometimes difficult to tell the difference between coin silver and silver plate. Coin silver was usually stamped with the first initial and last name of the silversmith. Occasionally, the silversmith also stamped the city and state on the pieces, as well. For example, on the back of a large serving spoon the stamp might read: "P.REVERE BOSTON, MA" inside a large rectangular punch. Often, it is necessary to consult one of several books available on the subject to determine whether hallmarks are indicative of coin silver items.</div>
