		<p class="fancybody">Luncheon Size vs. Place Size</p> 

<p>To determine whether you have either "Luncheon Size" or "Place Size" depends a great deal on who manufactured the flatware. For some Gorham Mfg. Co. patterns (Chantilly, Buttercup, Strasbourg, and Melrose) "Place Size" settings are designated by a "P" inside of a diamond on the back of the forks and on the stainless blades. If this is not the case and your fork measures 7 3/8" or smaller and your knife measures 9 1/4" or under, then you have the ""Luncheon Size" setting.</p>
<p>For patterns made by International Silver Co., the knife is the only piece that is substantially different in the "Place Size" vs. the "Luncheon Size." The "Place Size" knife will usually measure about 9 1/8" and always has a "modern" blade style (see info on blade styles) and longer handle, whereas the "Luncheon Size" knife will usually measure about 8 3/4" in International Silver flatware patterns. For all other manufacturers, read the next section on "Dinner Size." If your pieces do not meet the required length to be classified as "Dinner Size" according to the next section, then your pieces are probably "Luncheon Size." Some manufacturers only made a "Place Size." This was usually the case if the pattern was conceived in the 1950s.</p>


<p class="fancybody">Dinner Size</p>

<p>All flatware manufacturers offered the larger, "Dinner Size" pieces, which are more expensive to purchase. Again, the salad fork and teaspoon are the same size no matter what size the place setting. If your fork measures 7 1/2" or over and your knife measures 9 1/2" or over, then you have the "Dinner Size" setting. The only exceptions are patterns such as Grande Baroque by Wallace, La Scala by Gorham and El Grande by Towle, which have a lunch size fork measuring 7 1/2". However, even in these patterns, the "Lunch Size" or "Place Size" knife is under 9 1/2".</p>
